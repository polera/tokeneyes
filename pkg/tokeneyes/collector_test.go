func TestCollectorWorkersPreserveDeterministicResults(t *testing.T) {
	root := t.TempDir()
	files := map[string][]byte{
		"a.txt":        []byte("alpha\r\n"),
		"b.bin":        {0, 1, 2},
		"generated.go": []byte("// Code generated by test. DO NOT EDIT.\npackage generated"),
		"report.pdf":   []byte("%PDF-1.4\n1 0 obj << /Type /Page /MediaBox [0 0 612 792] >> stream\nBT (hello pdf) Tj ET\nendstream\nendobj\n%%EOF"),
		"z.txt":        []byte("omega"),
	}
	for name, content := range files {
		if err := os.WriteFile(filepath.Join(root, name), content, 0o600); err != nil {
			t.Fatal(err)
		}
	}
	if err := os.Symlink(filepath.Join(root, "a.txt"), filepath.Join(root, "link.txt")); err != nil {
		t.Fatal(err)
	}
	paths := []string{"z.txt", "report.pdf", "link.txt", "generated.go", "b.bin", "a.txt", "a.txt"}
	base := CollectRequest{Root: root, Paths: paths, MaxTotalBytes: 256, MaxMediaCount: 1, Workers: 1}
	want, err := NewFileCollector().Collect(context.Background(), base)
	if err != nil {
		t.Fatal(err)
	}
	for i := 0; i < 20; i++ {
		request := base
		request.Workers = 8
		got, err := NewFileCollector().Collect(context.Background(), request)
		if err != nil {
			t.Fatal(err)
		}
		if !reflect.DeepEqual(got, want) {
			t.Fatalf("parallel result differs on run %d:\ngot  %+v\nwant %+v", i, got, want)
		}
	}
}

func TestCollectorWorkersPreserveOrderedLimits(t *testing.T) {
	root := t.TempDir()
	for _, name := range []string{"a.txt", "b.txt", "c.txt"} {
		if err := os.WriteFile(filepath.Join(root, name), []byte("1234"), 0o600); err != nil {
			t.Fatal(err)
		}
	}
	one, err := NewFileCollector().Collect(context.Background(), CollectRequest{Root: root, MaxTotalBytes: 7, Workers: 1})
	if err != nil {
		t.Fatal(err)
	}
	many, err := NewFileCollector().Collect(context.Background(), CollectRequest{Root: root, MaxTotalBytes: 7, Workers: 8})
	if err != nil {
		t.Fatal(err)
	}
	if !reflect.DeepEqual(many, one) {
		t.Fatalf("parallel limits differ:\nmany %+v\none  %+v", many, one)
	}
	if len(many.Sources) != 1 || many.Sources[0].Path != "a.txt" || len(many.Warnings) != 2 {
		t.Fatalf("unexpected ordered limit result: %+v", many)
	}
}

func TestProcessCandidatesIsConcurrentAndCommitsInOrder(t *testing.T) {
	candidates := make([]fileCandidate, 4)
	for i := range candidates {
		candidates[i] = fileCandidate{sequence: i, eventIndex: i, rel: fmt.Sprintf("%d.txt", i), size: 1}
	}
	started := make(chan struct{}, len(candidates))
	release := make(chan struct{})
	var releaseOnce sync.Once
	prepare := func(candidate fileCandidate) preparedFile {
		started <- struct{}{}
		<-release
		return preparedFile{candidate: candidate, content: []byte("x")}
	}
	var committed []string
	done := make(chan error, 1)
	go func() {
		done <- processCandidates(context.Background(), candidates, 4, CollectRequest{MaxFileBytes: 10, MaxMediaBytes: 10, MaxTotalBytes: 10}, &Collection{}, prepare, func(result preparedFile) {
			committed = append(committed, result.candidate.rel)
		})
	}()
	for range candidates {
		select {
		case <-started:
		case <-time.After(2 * time.Second):
			releaseOnce.Do(func() { close(release) })
			t.Fatal("workers did not start concurrently")
		}
	}
	releaseOnce.Do(func() { close(release) })
	if err := <-done; err != nil {
		t.Fatal(err)
	}
	want := []string{"0.txt", "1.txt", "2.txt", "3.txt"}
	if !reflect.DeepEqual(committed, want) {
		t.Fatalf("commit order=%v, want %v", committed, want)
	}
}

